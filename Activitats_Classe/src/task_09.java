public class task_09 {
    public static void main(String[] args) {
        int[][] vector = new int[10][5];

        for (int i = 0; i < vector.length; i++) {
            for (int j = 0; j < vector[i].length ; j++) {
                System.out.println(" ");
                System.out.print(vector[i][j]);
            }
        }
    }
}
